function changeBg(){
    var navbar = document.getElementById('navbar');
    var scrollvalue = window.scrollY;
    if(scrollvalue<100){
      navbar.classList.remove('bgColor');
    }else{
      navbar.classList.add('bgColor');
    }
  }
  window.addEventListener('scroll',changeBg);
   
